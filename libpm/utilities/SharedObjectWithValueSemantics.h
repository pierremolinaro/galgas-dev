class SharedObjectWithValueSemantics : public SharedObject {

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //  Default constructor
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  
  protected: SharedObjectWithValueSemantics (LOCATION_ARGS) : SharedObject (THERE) { }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //  Protected copy constructor
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  protected: SharedObjectWithValueSemantics (const SharedObjectWithValueSemantics &) :
  SharedObject (HERE) { // Invoke default constructor
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //  No assignment
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  private: SharedObjectWithValueSemantics & operator = (const SharedObjectWithValueSemantics &) = delete ;

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //  Default destructor
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  protected: virtual ~ SharedObjectWithValueSemantics (void) = default ;

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //  Abstractor clone method
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  protected: virtual SharedObjectWithValueSemantics * cloned (void) const = 0 ;

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //  Friend
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  template <typename TYPE> friend class SharedPtrWithValueSemantics ;

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

} ;

//--------------------------------------------------------------------------------------------------
// SharedPtrWithValueSemantics template class
//--------------------------------------------------------------------------------------------------

template <typename TYPE> class SharedPtrWithValueSemantics {
//  static_assert (std::is_base_of <SharedObjectWithValueSemantics, TYPE>::value, "TYPE doit dériver de SharedObjectWithValueSemantics");

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Private property
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  private: TYPE * mSharedPtr ;

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Default constructor
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: SharedPtrWithValueSemantics (void) : mSharedPtr (nullptr) { }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Virtual destructor
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: ~ SharedPtrWithValueSemantics (void) {
    SharedObject::release (mSharedPtr) ;
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Set to nil
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: void setToNil (void) {
    SharedObject::release (mSharedPtr) ;
    mSharedPtr = nullptr ;
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Make
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: template <class... _Args> static SharedPtrWithValueSemantics <TYPE> make (_Args&&... __args) {
    SharedPtrWithValueSemantics <TYPE> result ;
    macroMyNew (result.mSharedPtr, TYPE (std::forward<_Args>(__args)...)) ;
    return result ;
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Accessors
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: inline bool isNotNil (void) const { return mSharedPtr != nullptr ; }
  public: inline bool isNil (void) const { return mSharedPtr == nullptr ; }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: TYPE * operator -> (void) {
    if (!mSharedPtr->isUniquelyReferenced ()) {
      SharedObject::release (mSharedPtr) ;
      mSharedPtr = mSharedPtr->cloned () ;
    }
    return mSharedPtr ;
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: const TYPE * operator -> (void) const { return mSharedPtr ; }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Copy constructor from SharedPtrWithValueSemantics <TYPE>
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: SharedPtrWithValueSemantics (const SharedPtrWithValueSemantics <TYPE> & inSource) :
  mSharedPtr (inSource.mSharedPtr) {
    SharedObject::retain (mSharedPtr) ;
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Copy constructor from SharedPtrWithValueSemantics <SOURCE>
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: template <typename SOURCE>
  SharedPtrWithValueSemantics (const SharedPtrWithValueSemantics <SOURCE> & inSource) :
  mSharedPtr (inSource.mSharedPtr) {
    static_assert (std::is_base_of <TYPE, SOURCE>::value, "SOURCE doit dériver de TYPE");
    SharedObject::retain (mSharedPtr) ;
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Assignment from SharedPtrWithValueSemantics <TYPE>
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: SharedPtrWithValueSemantics <TYPE> & operator = (const SharedPtrWithValueSemantics <TYPE> & inSource) {
    SharedObject::retain (inSource.mSharedPtr) ;
    SharedObject::release (mSharedPtr) ;
    mSharedPtr = inSource.mSharedPtr ;
    return *this ;
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Assignment from SharedPtrWithValueSemantics <SOURCE>
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  public: template <typename SOURCE>
  SharedPtrWithValueSemantics <TYPE> & operator = (const SharedPtrWithValueSemantics <SOURCE> & inSource) {
    static_assert (std::is_base_of <TYPE, SOURCE>::value, "SOURCE doit dériver de TYPE");
    SharedObject::retain (inSource.mSharedPtr) ;
    SharedObject::release (mSharedPtr) ;
    mSharedPtr = inSource.mSharedPtr ;
    return *this ;
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  //   Friends
  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  template <typename SOURCE> friend class SharedPtrWithValueSemantics ;

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

} ;

